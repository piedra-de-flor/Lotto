package LottoGame.Lotto.vo;

import java.util.List;

public class Lottos {
    private final List<Lotto> lottos;

    public Lottos(List<Lotto> lottos) {
        this.lottos = lottos;
    }

    public List<Lotto> getLottos() {
        return lottos;
    }

    public void add(Lotto lotto) {
        lottos.add(lotto);
    }
}